const normalize = (str) => {
  if (!str) return '';
  return str.trim()
    .replace(/[أإآ]/g, 'ا')
    .replace(/ة/g, 'ه')
    .replace(/ى/g, 'ي')
    .toLowerCase();
};

async function request(path, opts = {}) {
  const headers = { 'Content-Type': 'application/json', 'x-admin-key': ADMIN_KEY };
  const res = await fetch(`${API_BASE}${path}`, { ...opts, headers });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`HTTP ${res.status}: ${text}`);
  }
  return res.json();
}

async function main() {
  console.log('🔄 Fetching current data from API...');
  const collections = await request('/collections');
  const products = await request('/products?admin=true&limit=1000');
  
  const colMap = {};
  collections.forEach(c => colMap[normalize(c.name)] = c);
  
  const prodMap = {};
  products.products.forEach(p => prodMap[normalize(p.name)] = p);
  
  console.log(`✅ Loaded ${collections.length} collections and ${products.products.length} products.`);
  
  for (const [colName, orderedProductNames] of Object.entries(CRAWL_DATA)) {
    const col = colMap[normalize(colName)];
    if (!col) {
      console.warn(`⚠️ Collection not found: ${colName}`);
      continue;
    }
    
    const productOrder = [];
    for (const pName of orderedProductNames) {
      const p = prodMap[normalize(pName)];
      if (p) {
        productOrder.push(p._id);
      } else {
        // console.warn(`   - Product not found in ${colName}: ${pName}`);
      }
    }
    
    if (productOrder.length > 0) {
      console.log(`🚀 Updating order for ${colName} (${productOrder.length} products)...`);
      await request(`/collections/${col._id}`, {
        method: 'PUT',
        body: JSON.stringify({ productOrder })
      });
    } else {
      console.warn(`⚠️ No products found for collection: ${colName}`);
    }
  }
  
  console.log('✨ Sync complete!');
}

main().catch(err => console.error('❌ Error:', err.message));
